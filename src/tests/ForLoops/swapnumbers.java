package tests.ForLoops;

public class swapnumbers {

    public static void main(String[] args) {
        int a,b;
        a=10;
        b=20;

        System.out.println("a = " + a);
        System.out.println("b = " + b);
        int c;
        c=a;
        a=b;
        b=c;

        System.out.println("a = " + a);
        System.out.println("b = " + b);
    }
}
